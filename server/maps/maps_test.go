func TestMaps(t *testing.T) {
	// TODO: run parallel
	t.Parallel()

	{
		_, err := maps.NewMap(netip.Addr{})
		require.Error(t, err)
	}

	{
		m, err := maps.NewMap(netip.MustParseAddr("127.0.0.1"))
		require.NoError(t, err)
		defer func() { require.NoError(t, m.Clsoe()) }()

		src, dst := randIPPort(), randIPPort()
		locPort, new, err := m.UpGetUDP(
			src,
			dst,
		)
		require.NoError(t, err)
		require.True(t, new)

		gSrc, has := m.DownGetUDP(dst, locPort)
		require.True(t, has)
		require.Equal(t, src, gSrc)
	}

	{
		m, err := maps.NewMap(netip.MustParseAddr("127.0.0.1"))
		require.NoError(t, err)
		defer func() { require.NoError(t, m.Clsoe()) }()

		src := randIPPort()
		dst1, dst2 := randIPPort(), randIPPort()
		locPort1, new, err := m.UpGetUDP(
			src,
			dst1,
		)
		require.NoError(t, err)
		require.True(t, new)
		locPort2, new, err := m.UpGetUDP(
			src,
			dst2,
		)
		require.NoError(t, err)
		require.True(t, new)

		gSrc1, has := m.DownGetUDP(dst1, locPort1)
		require.True(t, has)
		require.Equal(t, src, gSrc1)

		gSrc2, has := m.DownGetUDP(dst2, locPort2)
		require.True(t, has)
		require.Equal(t, src, gSrc2)

		_, has = m.DownGetUDP(dst1, locPort2)
		require.False(t, has)
		_, has = m.DownGetUDP(dst2, locPort1)
		require.False(t, has)
	}
}

